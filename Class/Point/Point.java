public class Point {
	
	private int x, y;

	public Point (int x, int y) {

		this.x = x;
		this.y = y;
	}

	public int getPositionX () {

		return this.x;
	}

	public void setPositionX (int x) {

		this.x = x;
	}

	public int getPositionY () {

		return this.y;
	}

	public void setPositionY (int y) {
		
		this.y = y;
	}
}